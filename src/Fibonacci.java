import java.util.Scanner;

public class Fibonacci {
    public static void main(String[] args) {

      int a = 5;
      int b = 7;

        System.out.println("a & b = " + (a & b));  // Bitwise And
        System.out.println("a | b = " + (a | b));   //Bitwise OR
        System.out.println("a ^ b = " + (a ^ b));    //Bitwise XOR
        System.out.println("~a = " + (~a));  // Bitwise Not / Complement Operator , compiler will return 2's complement answer which is(-6)
// Bitwise And   :  If both bits are 1, it gives 1, else it gives 0.
// Bitwise OR    :  if either of the bits is 1, it gives 1, else it gives 0.
//Bitwise XOR    : if corresponding bits are different, it gives 1, else it gives 0.
// Bitwise Not   :  ‘~’. It returns the one’s complement representation of the input value,
// i.e, with all bits inverted, which means it makes every 0 to 1, and every 1 to 0.


    }
}
